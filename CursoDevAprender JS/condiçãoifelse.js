// If... Else
//Se a hora estiver entre 06:00 até 12:00 : Bom dia!
//Se estiver entre 12:00 até 18:00 : Boa Tarde!
//Se não for nenhum destes casos : Boa Noite!

let hora = 22;

if(hora > 6 && hora < 12 ){
    console.log("Bom Dia!");
}else if (hora > 12 && hora < 18){
    console.log("Boa Tarde!")
}
else {
    console.log("Boa Noite!")
}










